namespace ClcLib;



public class Calculator
{
	public int Add(int a, int b)
	{
		return(a+b);
	}
	public int Multiply(int a, int b)
	{
		return(a*b);
	}
	
	public int Divide(int a, int b)
	{
		return(a/b);
	}
	public float Divide(float a, float b)
	{
		return (a/b);
	}
	public int Minus(int a, int b)
	{
		return(a-b);
	}
	
	
}

